function J = bykov_dxdx(x,p)

x1 = x(1,:); 
p1 = p(1,:); 
p3 = p(3,:); 
p5 = p(5,:); 

J = zeros(3,3,3,numel(x1));
J(1,1,1,:) = 4*p1-4*p5;
J(1,1,2,:) = 4*p1-p3;
J(1,1,3,:) = 4*p1;
J(2,1,2,:) = -p3;
J(1,2,1,:) = 4*p1-p3;
J(1,2,2,:) = 4*p1;
J(1,2,3,:) = 4*p1;
J(2,2,1,:) = -p3;
J(1,3,1,:) = 4*p1;
J(1,3,2,:) = 4*p1;
J(1,3,3,:) = 4*p1;

end
